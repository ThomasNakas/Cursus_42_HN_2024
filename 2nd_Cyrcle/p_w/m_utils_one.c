#include "push_swap.h"

int	m_abs(int a, int b)
{
	return ((a - b) * ((a - b >= 0) + (-1) * (a - b < 0)));
}

int	m_psqrt(int a)
{
	int	i;

	i = 1;
	while ((i + 1) * (i + 1) <= a)
		i++;
	return (i + 1);
}

int	m_kvar(int a)
{
	return ((m_psqrt(a) * (13 / 10)));
}
